package pansong291.img2html4android.other;

public class RGB
{
 private int r,g,b;
 public RGB(){}
 public RGB(int r,int g,int b)
 {
  this.r=r;this.g=g;this.b=b;
 }

 public void setR(int r)
 {
  this.r = r;
 }

 public int getR()
 {
  return r;
 }

 public void setG(int g)
 {
  this.g = g;
 }

 public int getG()
 {
  return g;
 }

 public void setB(int b)
 {
  this.b = b;
 }

 public int getB()
 {
  return b;
 }
}
